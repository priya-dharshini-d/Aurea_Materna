export function getArticleData(articleId: string) {
  const categoryId = articleId.split('-')[0];
  
  // Need to search in all categories since we might be requesting a fallback
  if (categoriesData[categoryId]) {
    const article = categoriesData[categoryId].articles.find((a: any) => a.id === articleId);
    if (article) {
      return { ...article, categoryTitle: categoriesData[categoryId].title };
    }
  }
  
  // If we can't find it directly, try looking everywhere (in case ID format varies)
  for (const catKey of Object.keys(categoriesData)) {
    const cat = categoriesData[catKey];
    const found = cat.articles.find((a: any) => a.id === articleId);
    if (found) {
      return { ...found, categoryTitle: cat.title };
    }
  }

  // Ultimate fallback
  const cleanTitle = articleId.replace(/-/g, ' ');
  return {
    id: articleId,
    title: `Information regarding ${cleanTitle}`,
    desc: 'Important details and guidance for expectant mothers.',
    categoryTitle: 'Pregnancy Guide',
    image: require('../assets/images/img_cond1.png'),
    content: [
      { type: 'text', value: 'This is a dynamically generated article placeholder for your requested topic.' },
      { type: 'subheading', value: 'Guidance' },
      { type: 'text', value: 'Always consult your primary care provider for medical advice.' }
    ]
  };
}
